class Product {
  final String image;
  final String name;
  final String description;
  final double price;
  final double rating;
  final int stock;
  final String discount;
  final String promoCode;

  Product({
    required this.image,
    required this.name,
    required this.description,
    required this.price,
    required this.rating,
    required this.stock,
    required this.discount,
    required this.promoCode,
  });
}